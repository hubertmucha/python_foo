def f01_12_charListToStr(l):
    """
    Merges list of chars into a string

    Parameters
    ----------
    l : list
        List of chars (can be empty)

    Returns
    -------
    r : string
    """
    
    str = ""
    for char in l:
        str = str+char
    return str
