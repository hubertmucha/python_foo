def f02_15_string2charList(s):
    """
    Converts the string into the list of chars

    Parameters
    ----------
    s : str
        Any string.
    
    Returns
    -------
    list
        list of characters of the input string
        
    Implementation constraints
    --------------------------
    - target number of non-white characters for the code: 42
      (docstring and comments are ignored when counting)
    - do not use "import"
    """
    return list(s)
